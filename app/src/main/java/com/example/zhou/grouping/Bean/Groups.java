package com.example.zhou.grouping.Bean;

public class Groups {
	private String gID;
	private String gName;
	private String gPasswd;
	private int gMin;
	private int gMax;

	public String getgID() {
		return gID;
	}

	public void setgID(String gID) {
		this.gID = gID;
	}

	public String getgName() {
		return gName;
	}

	public void setgName(String gName) {
		this.gName = gName;
	}

	public String getgPasswd() {
		return gPasswd;
	}

	public void setgPasswd(String gPasswd) {
		this.gPasswd = gPasswd;
	}

	public int getgMin() {
		return gMin;
	}

	public void setgMin(int gMin) {
		this.gMin = gMin;
	}

	public int getgMax() {
		return gMax;
	}

	public void setgMax(int gMax) {
		this.gMax = gMax;
	}

}
